console.log(Math.pow(2,5)) //Sirve para elevar un numero (al cuadrado, al cubo o a lo que queramos)
console.log(Math.sign(-7.8)) //Sirve para indicar si un numero es negativo o positivo o cero
console.log(Math.random()*1000) //Devuelve un numero random entre 0 y 1 por eso se lo multiplica por el numero que se quiera conseguir, es normal utilizarlo con el math.round
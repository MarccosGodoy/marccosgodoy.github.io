console.log(Math.abs(7.8)) //Devuelve el valor absoluto de un numero
console.log(Math.ceil(7.8)) //Redondea el numero al entero mas grande que le sigue
console.log(Math.floor(7.8)) //Redondea al numero mas chico
console.log(Math.round(7.8)) //Redondea al numero entero mas cercano
